package day4;

public class Employee {
    public String name;
    public  int age;
    public  int SSN;

    public void setName(String name) {
        this.name = name;
    }

    public void setAge(int age) {
        this.age = age;
    }

    public void setSSN(int SSN) {
        this.SSN = SSN;
    }

    public String getName() {
        return name;
    }

    public int getAge() {
        return age;
    }

    public int getSSN() {
        return SSN;
    }
}
